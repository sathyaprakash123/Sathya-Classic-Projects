package Dhiber;

public class Employee {
	
	private String name, gender;
	private int age;
	
	public String getName()
	{
		return name;
	}
	
	
	public String getGender()
	{
		return gender;
	}
	
	public int getAge()
	{
		return age;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	
	public void setGender(String gender)
	{
		this.gender = gender;
	}
	
	public void setAge(int age)
	{
		this.age = age;
	}

}
